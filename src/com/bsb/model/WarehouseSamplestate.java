package com.bsb.model;

public class WarehouseSamplestate {
	private String Storage_conditions;
	private String Warehouse_area;
	private int Warehouse_total;
	private int Warehouse_balance;
	
	public String getStorage_conditions() {
		return Storage_conditions;
	}
	
	public void setStorage_conditions(String _Storage_conditions) {
		Storage_conditions = _Storage_conditions;
	}
	
	public String getWarehouse_area() {
		return Warehouse_area;
	}
	
	public void setWarehouse_area(String _Warehouse_area) {
		Warehouse_area = _Warehouse_area;
	}
	
	public int getWarehouse_balance() {
		return Warehouse_balance;
	}
	
	public void setWarehouse_balance(int _Warehouse_balance) {
		Warehouse_balance = _Warehouse_balance;
	}
	
	public int getWarehouse_total() {
		return Warehouse_total;
	}
	
	public void setWarehouse_total(int _Warehouse_total) {
		Warehouse_total = _Warehouse_total;
	}
}
